/*
 * bitAnd - x&y using only ~ and |
 *   Example: bitAnd(6, 5) = 4
 *   Legal ops: ~ |
 *   Max ops: 8
 *   Rating: 1
 */
//simple deMorgan law
int bitAnd(int x, int y) {
  return ~(~x | ~y);
}
/*
 * bitNor - ~(x|y) using only ~ and &
 *   Example: bitNor(0x6, 0x5) = 0xFFFFFFF8
 *   Legal ops: ~ &
 *   Max ops: 8
 *   Rating: 1
 */
//simple deMorgan laws
int bitNor(int x, int y) {
  return ~x & ~y;
}
/*
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */

int byteSwap(int x, int n, int m) {
   int byteMask = 0x000000FF; /*a byte of ones, will be used to copy single bytes*/

   /*<<3 is the same as multiplying by 8
     nShift and mShift contain the number of bytes you will shift */
   int nShift = n << 3;
   int mShift = m << 3;

   /*shift a to the desired byte in x, & with x*/
   int nByte = (byteMask << nShift)&x;
   int mByte = (byteMask << mShift)&x;
   int combo = (byteMask << nShift) | (byteMask << mShift); /*1's in the swapping bytes*/

   /*shift swapping bytes back to the left*/
   nByte = (nByte >> nShift) & byteMask;
   mByte = (mByte >> mShift) & byteMask;
   /*shift swapping bytes to their new position*/
   nByte = nByte << mShift;
   mByte = mByte << nShift;
   /*store the nonchanging part of the int in "combo"*/
   combo = ~combo & x;

   return combo | mByte | nByte;  //
}
/*
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) {
    int bitsmask = (1 << n) + ~(0);
    int flip = (x >> 31) & bitsmask;

    return (x + flip) >> n;
}
/*
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
  // byte mask with LSB turned on
  int byteMask = 0x000000FF ;
  //shift byte n to LSB
  int xShift = x >> (n << 3);
  //return byte n
  return byteMask & xShift;
}
